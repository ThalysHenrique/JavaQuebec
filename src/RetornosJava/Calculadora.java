// Exercício sobre Retornos do métodos
package RetornosJava;

public class Calculadora {
    public static int Calculadora(int comprimento){
        return comprimento * comprimento;
    }
    public static int Calculadora(int base, int altura){
        return base * altura;
    }
    public static int Calculadora(int altura, int base, int Base){
       return (altura * (base + Base)) / 2;
    }
}
